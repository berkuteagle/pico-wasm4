void w4_framebuffer_line(uint8_t *framebuffer, const uint8_t *const draw_colors, uint32_t x1, uint32_t y1, uint32_t x2, uint32_t y2)
{
    uint8_t dc0 = draw_colors[0] & 0xf;
    if (dc0 == 0)
    {
        return;
    }
    uint8_t stroke_color = (dc0 - 1) & 0x3;

    if (y1 > y2)
    {
        int swap = x1;
        x1 = x2;
        x2 = swap;

        swap = y1;
        y1 = y2;
        y2 = swap;
    }

    int dx = w4_abs(x2 - x1), sx = x1 < x2 ? 1 : -1;
    int dy = y2 - y1;
    int err = (dx > dy ? dx : -dy) / 2, e2;

    for (;;)
    {
        w4_draw_point_unclipped(framebuffer, stroke_color, x1, y1);
        if (x1 == x2 && y1 == y2)
        {
            break;
        }
        e2 = err;
        if (e2 > -dx)
        {
            err -= dy;
            x1 += sx;
        }
        if (e2 < dy)
        {
            err += dx;
            y1++;
        }
    }
}

void w4_framebuffer_hline(uint8_t *framebuffer, const uint8_t *const draw_colors, uint32_t x, uint32_t y, uint32_t len)
{
    uint8_t dc0 = draw_colors[0] & 0xf;
    if (dc0 == 0)
    {
        return;
    }

    uint8_t stroke_color = (dc0 - 1) & 0x3;
    w4_draw_hline_unclipped(framebuffer, stroke_color, x, y, x + len);
}

void w4_framebuffer_vline(uint8_t *framebuffer, const uint8_t *const draw_colors, uint32_t x, uint32_t y, uint32_t len)
{
    if (y + len <= 0 || x < 0 || x >= W4_FB_W)
    {
        return;
    }

    uint8_t dc0 = draw_colors[0] & 0xf;
    if (dc0 == 0)
    {
        return;
    }

    int startY = w4_max(0, y);
    int endY = w4_min(W4_FB_H, y + len);
    uint8_t stroke_color = (dc0 - 1) & 0x3;
    for (int yy = startY; yy < endY; yy++)
    {
        w4_draw_point(framebuffer, stroke_color, x, yy);
    }
}

void w4_framebuffer_rect(uint8_t *framebuffer, const uint8_t *const draw_colors, uint32_t x, uint32_t y, uint32_t width, uint32_t height)
{
    int startX = w4_max(0, x);
    int startY = w4_max(0, y);
    int endXUnclamped = x + width;
    int endYUnclamped = y + height;
    int endX = w4_max(0, w4_min(endXUnclamped, W4_FB_W));
    int endY = w4_max(0, w4_min(endYUnclamped, W4_FB_H));

    uint8_t dc01 = draw_colors[0];
    uint8_t dc0 = dc01 & 0xf;
    uint8_t dc1 = (dc01 >> 4) & 0xf;

    if (dc0 != 0)
    {
        uint8_t fill_color = (dc0 - 1) & 0x3;
        for (int yy = startY; yy < endY; ++yy)
        {
            w4_draw_hline(framebuffer, fill_color, startX, yy, endX);
        }
    }

    if (dc1 != 0)
    {
        uint8_t stroke_color = (dc1 - 1) & 0x3;

        // Left edge
        if (x >= 0 && x < W4_FB_W)
        {
            for (int yy = startY; yy < endY; ++yy)
            {
                w4_draw_point(framebuffer, stroke_color, x, yy);
            }
        }

        // Right edge
        if (endXUnclamped > 0 && endXUnclamped <= W4_FB_W)
        {
            for (int yy = startY; yy < endY; ++yy)
            {
                w4_draw_point(framebuffer, stroke_color, endXUnclamped - 1, yy);
            }
        }

        // Top edge
        if (y >= 0 && y < W4_FB_H)
        {
            w4_draw_hline(framebuffer, stroke_color, startX, y, endX);
        }

        // Bottom edge
        if (endYUnclamped > 0 && endYUnclamped <= W4_FB_H)
        {
            w4_draw_hline(framebuffer, stroke_color, startX, endYUnclamped - 1, endX);
        }
    }
}

void w4_framebuffer_oval(uint8_t *framebuffer, const uint8_t *const draw_colors, uint32_t x, uint32_t y, uint32_t width, uint32_t height)
{
    uint8_t dc01 = draw_colors[0];
    uint8_t dc0 = dc01 & 0xf;
    uint8_t dc1 = (dc01 >> 4) & 0xf;

    if (dc1 == 0xf)
    {
        return;
    }

    uint8_t stroke_color = (dc1 - 1) & 0x3;
    uint8_t fill_color = (dc0 - 1) & 0x3;

    int a = width - 1;
    int b = height - 1;
    int b1 = b % 2; // Compensates for precision loss when dividing

    int north = y + height / 2; // Precision loss here
    int west = x;
    int east = x + width - 1;
    int south = north - b1; // Compensation here. Moves the bottom line up by
                            // one (overlapping the top line) for even heights

    // Error increments. Also known as the decision parameters
    const int a2 = a * a;
    const int b2 = b * b;

    int dx = 4 * (1 - a) * b2;
    int dy = 4 * (b1 + 1) * a2;

    // Error of 1 step
    int err = dx + dy + b1 * a2;

    a = 8 * a2;
    b1 = 8 * b2;

    do
    {
        w4_draw_point_unclipped(framebuffer, stroke_color, east, north); /* I. Quadrant */
        w4_draw_point_unclipped(framebuffer, stroke_color, west, north); /* II. Quadrant */
        w4_draw_point_unclipped(framebuffer, stroke_color, west, south); /* III. Quadrant */
        w4_draw_point_unclipped(framebuffer, stroke_color, east, south); /* IV. Quadrant */

        const int start = west + 1;
        const int len = east - start;

        if (dc0 != 0 &&
            len > 0)
        {
            // Only draw fill if the length from west to east is not 0
            w4_draw_hline_unclipped(framebuffer, fill_color, start, north, east); /* I and III. Quadrant */
            w4_draw_hline_unclipped(framebuffer, fill_color, start, south, east); /* II and IV. Quadrant */
        }

        const int err2 = 2 * err;

        if (err2 <= dy)
        {
            // Move vertical scan
            north += 1;
            south -= 1;
            dy += a;
            err += dy;
        }

        if (err2 >= dx || err2 > dy)
        {
            // Move horizontal scan
            west += 1;
            east -= 1;
            dx += b1;
            err += dx;
        }
    } while (west <= east);

    // Make sure north and south have moved the entire way so top/bottom aren't
    // missing
    while (north - south < height)
    {
        w4_draw_point_unclipped(framebuffer, stroke_color, west - 1, north); /*   II. Quadrant    */
        w4_draw_point_unclipped(framebuffer, stroke_color, east + 1, north); /*   I. Quadrant     */
        north += 1;
        w4_draw_point_unclipped(framebuffer, stroke_color, west - 1, south); /*   III. Quadrant   */
        w4_draw_point_unclipped(framebuffer, stroke_color, east + 1, south); /*   IV. Quadrant    */
        south -= 1;
    }
}